package utils;

/**
 * The Class Chars.
 * This class contains special characters.
 *
 */
public class Chars {

    /** The union. */
    public static Character union = '|';

    /** The open parenthesis. */
    public static Character open_parenthesis = '(';

    /** The close parenthesis. */
    public static Character close_parenthesis = ')';

    /** The Kleene star. */
    public static Character kleene_star = '*';

    /** The epsilon. */
    public static Character epsilon = '\u03B5';

    /** The concatenation. */
    public static Character concatenation = '.';

    /** The none. */
    public static Character none = '-';

    /** The comma. */
    public static Character comma = ',';

    /** The space. */
    public static String space = " ";

    /** The empty. */
    public static String empty = "";
}
